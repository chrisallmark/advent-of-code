const day = require("./index.ts");

const input = day.loadInput("day10.txt");

describe("Day 10", () => {
  test("Load Input", () => {
    expect(input).toMatch(
      /^addx 15\naddx -11\naddx 6\naddx -3\naddx 5\naddx -1\naddx -8\naddx 13\naddx 4\nnoop/
    );
  });

  test("Crunch Input", () => {
    expect(day.crunchInput(input)).toEqual([
      13140,
      "##..##..##..##..##..##..##..##..##..##..\n###...###...###...###...###...###...###.\n####....####....####....####....####....\n#####.....#####.....#####.....#####.....\n######......######......######......####\n#######.......#######.......#######.....",
    ]);
  });

  test("Solve", () => {
    expect(day.solve(input)).toEqual([
      13140,
      "##..##..##..##..##..##..##..##..##..##..\n###...###...###...###...###...###...###.\n####....####....####....####....####....\n#####.....#####.....#####.....#####.....\n######......######......######......####\n#######.......#######.......#######.....",
    ]);
  });
});

